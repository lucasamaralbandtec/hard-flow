    public JfTarefas() {
        initComponents();
        
        long TEMPO = (1000 * 1);
    
    Timer timer = null;
    
    if(timer == null)
    {
        timer = new Timer();
        
        TimerTask tarefa = new TimerTask(){
            public void run(){
                
                
                porcentagemRAM();
                porcentagemCPU();
                discoLivre();
            }
        
        };
        timer.scheduleAtFixedRate(tarefa, TEMPO, TEMPO);
    }
}
    
  private void porcentagemRAM()
    {
        Double capturaRam = ram.getPorcentagemRam();
        lbl_RAM.setText(String.format("%.1f%%", capturaRam));
    }

    private void porcentagemCPU()
    {
        Double cpuPorcentagem = cpu.getPorcentagemCPU();
        lbl_CPU.setText(String.format("%.1f%%", cpuPorcentagem));
    }  
    
    private void discoLivre()
    {
        String capturaDisco = disco.getDiscoDisponivel();
        lbl_DISCO.setText(capturaDisco);
    }
    
    

